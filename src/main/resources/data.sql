DROP TABLE IF EXISTS TBL_ESTUDIANTES;
CREATE TABLE TBL_ESTUDIANTES (
    ID_ESTUDIANTE INT PRIMARY KEY,
    NOMBRE VARCHAR(250) NOT NULL,
    ID_CURSO INT NOT NULL
);
DROP TABLE IF EXISTS TBL_CURSOS_ASIGNATURAS;
CREATE TABLE TBL_CURSOS_ASIGNATURAS(
    ID INT AUTO_INCREMENT PRIMARY KEY,
    ID_CURSO INT NOT NULL,
    ID_ASIGNATURA INT NOT NULL
);

DROP TABLE IF EXISTS TBL_CURSOS;
CREATE TABLE TBL_CURSOS (
    ID_CURSO INT PRIMARY KEY,
    GRADO INT NOT NULL,
    SALON VARCHAR(250) NOT NULL,
    ID_COLEGIO INT NOT NULL
);
DROP TABLE IF EXISTS TBL_COLEGIOS;
CREATE TABLE TBL_COLEGIOS (
    ID_COLEGIO INT PRIMARY KEY,
    NOMBRE VARCHAR(250) NOT NULL
);

DROP TABLE IF EXISTS TBL_ASIGNATURA;
CREATE TABLE TBL_ASIGNATURA (
    ID_ASIGNATURA INT PRIMARY KEY,
    NOMBRE VARCHAR(250) NOT NULL,
    ID_PROFESOR INT NOT NULL
);
DROP TABLE IF EXISTS TBL_PROFESORES;
CREATE TABLE TBL_PROFESORES (
    ID_PROFESOR INT PRIMARY KEY,
    NOMBRE VARCHAR(250) NOT NULL
);


